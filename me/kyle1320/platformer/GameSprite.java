	/**	Creates a new GameSprite from the given Sprite
		@param sprite the Sprite represented by this GameSprite
	*/
	private GameSprite(Sprite sprite) {
		this.sprite = sprite;
	}

	/**	Returns a copy of this GameSprite's sprite.
		@return a copy of this GameSprite's sprite.
	*/
	public Sprite getSprite() {
		return sprite.copy();
	}

	/**	Returns a paint created from this GameSprite's sprite for use in drawing.
		@return A TexturePaint created from this GameSprite's sprite
	*/
	public TexturePaint getTexture() {
		BufferedImage image = sprite.getImage();
		return new TexturePaint(image, new Rectangle(image.getWidth(), image.getHeight()));
	}
